    <h2 class="cursus">Overtaking</h2>
<ul>
    <li><span style="font-size: 14px;">Check if the road is clear using your mirrors, signal before acting</span></li>
    <li><span style="font-size: 14px;">Overtake quickly and move back to the right </span></li>
    <li><span style="font-size: 14px;">Do NOT accelerate while being overtaken </span></li>
    <li><span style="font-size: 14px;">Give way to cars coming to you from the opposite direction</span></li>
    <li><span style="font-size: 14px;">On motorways never overtake on the right side</span></li>
    <li><span style="font-size: 14px;">Do NOT overtake:</span>
    <ul>
        <li><span style="font-size: 14px;">when coming to a junction, a crossing, a corner or bend, a bridge or a hill </span></li>
        <li><span style="font-size: 14px;">when the road narrows </span></li>
        <li><span style="font-size: 14px;">when you might hinder another car</span></li>
        <li><span style="font-size: 14px;">when you would have to violate rules for lines and lanes</span></li>
        <li><span style="font-size: 14px;">when there is a &quot;</span><span style="font-size: 14px;">no overtaking&quot; road sign </span><span style="font-size: 14px;">
        </span></li>
    </ul>
    </li>
</ul>
