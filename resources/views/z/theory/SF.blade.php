    <h2 class="cursus">Road Junctions</h2>
<ul>
    <li><span style="font-size: 14px;">Look left, look right, look left again before crossing or turning; use your mirrors to double-check </span></li>
    <li><span style="font-size: 14px;">On a level crossing, give way to a car from the left </span></li>
    <li><span style="font-size: 14px;">On a priority junction: </span>
    <ul>
        <li><span style="font-size: 14px;">when you have NO priority, stop at the &quot;stop&quot; or &quot;give way&quot; road sign, wait for a safe gap in the traffic before moving on </span></li>
        <li><span style="font-size: 14px;">when you have priority, make sure other road users give way to you before moving on </span></li>
        <li><span style="font-size: 14px;">when a mandatory road sign is present, follow indicated direction </span></li>
    </ul>
    </li>
    <li><span style="font-size: 14px;">When controlled by traffic lights, follow the lights, but make sure other road users give way before moving on at a green light    </span></li>
    <li><span style="font-size: 14px;">When controlled by a police officer, follow his/her directions at all times    </span></li>
</ul>
